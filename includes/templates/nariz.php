<h2>Nariz</h2>

<li 
data-definition="Es cuando el 'muro' dentro de la nariz está torcido, lo que puede hacer que sea difícil respirar por un lado de la nariz."
data-type="image"
data-src="src/enfermedades/septal.jpg"
>Desviación septal o del tabique nasal
</li>

<li 
data-definition="Es el sangrado de la nariz, lo que todos conocemos como un 'sangrado nasal'"
data-type="image"
data-src="src/enfermedades/Epistaxis.jpg"
>Epixtaxis</li>

<li 
data-definition="Es sacar algo que se metió en la nariz y no debería estar ahí, como un juguete o comida."
data-type="image"
data-src="src/enfermedades/retiro_de_sueño.png"
>Retiro del cuerpo extraño</li>

<li 
data-definition="Es cuando la nariz se inflama y causa mocos, estornudos o picazón, ya sea porque algo te da alergia o porque el problema lleva mucho tiempo."
data-type="image"
data-src="src/enfermedades/rinitis.jpg"
>Rinitis (crónicas y alérgicas)</li>

<li 
data-definition="Es una infección o inflamación de los senos paranasales (espacios en los huesos de la cara), lo que causa dolor en la cara, congestión y a veces fiebre."
data-type="image"
data-src="src/enfermedades/sinusitis.webp"
>Sinusitis</li>

<li 
data-definition="Son bultos o crecimientos dentro de la nariz, que pueden ser benignos (no peligrosos) o malignos (cáncer)."
data-type="image"
data-src="src/enfermedades/tumores.jpg"
>Tumores nasales</li>

<li 
data-definition="Son pequeños bultos blandos que crecen dentro de la nariz o senos paranasales, normalmente por alergias o infecciones repetidas, y pueden dificultar la respiración."
data-type="image"
data-src="src/enfermedades/polipos.jpeg"
>Pólipos nasales</li>




